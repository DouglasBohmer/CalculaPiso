package Data;

public class Piso {

	private String nome;
	private String cod_asso;
	private String cod_ctc;
	private String largura;
	private String altura;
	private String rejunte;
	private String peca_cx;
	private String m_cx;
	private String local_uso;
	private String tipo_piso;
	private String pei;
	private String retificado;
	private String site;
	private String foto;
	private String ambiente;
	
	public Piso() {
		
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public String getCod_asso() {
		return cod_asso;
	}

	public void setCod_asso(String cod_asso) {
		this.cod_asso = cod_asso;
	}

	public String getCod_ctc() {
		return cod_ctc;
	}

	public void setCod_ctc(String cod_ctc) {
		this.cod_ctc = cod_ctc;
	}

	public String getLargura() {
		return largura;
	}

	public void setLargura(String largura) {
		this.largura = largura;
	}

	public String getAltura() {
		return altura;
	}

	public void setAltura(String altura) {
		this.altura = altura;
	}

	public String getRejunte() {
		return rejunte;
	}

	public void setRejunte(String rejunte) {
		this.rejunte = rejunte;
	}

	public String getPeca_cx() {
		return peca_cx;
	}

	public void setPeca_cx(String peca_cx) {
		this.peca_cx = peca_cx;
	}

	public String getM_cx() {
		return m_cx;
	}

	public void setM_cx(String m_cx) {
		this.m_cx = m_cx;
	}

	public String getLocal_uso() {
		return local_uso;
	}

	public void setLocal_uso(String local_uso) {
		this.local_uso = local_uso;
	}

	public String getTipo_piso() {
		return tipo_piso;
	}

	public void setTipo_piso(String tipo_piso) {
		this.tipo_piso = tipo_piso;
	}

	public String getPei() {
		return pei;
	}

	public void setPei(String pei) {
		this.pei = pei;
	}

	public String getRetificado() {
		return retificado;
	}

	public void setRetificado(String retificado) {
		this.retificado = retificado;
	}

	public String getSite() {
		return site;
	}

	public void setSite(String site) {
		this.site = site;
	}

	public String getFoto() {
		return foto;
	}

	public void setFoto(String foto) {
		this.foto = foto;
	}

	public String getAmbiente() {
		return ambiente;
	}

	public void setAmbiente(String ambiente) {
		this.ambiente = ambiente;
	}
	
	
}
